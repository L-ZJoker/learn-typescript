class Person{

}

class Student extends Person{
  studying() {}
}

const stu = new Student()

function foo (p: Person) {
  // p.studying() // 类型“Person”上不存在属性“studying”
  (p as Student).studying()
}
foo(stu)

function printLength(message?: string) {
  console.log(message!.length); // ! ->非空断言，告诉编译器我确定可选参数message不会传过来undefined
}
printLength('Hello ZJoker')

